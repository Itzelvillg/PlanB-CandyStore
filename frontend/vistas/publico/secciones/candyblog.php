      <div class="card-body">
        <h5 class="card-title">Flan de coco</h5>
        <p class="card-text"> <b>Ingredientes:</b><br>
          
            1 lata pequeña de leche condensada.<br>
            1 lata pequeña de leche de coco.<br>
            4 huevos.<br>
            1 cdta. de extracto de vainilla.<br>
            1 cdta. de extracto de coco.<br>
    
            Media taza de azúcar refinada.<br>
            <b> Preparación:</b> <br>
           1.  Precalienta el horno a una temperatura de 200°C.<br>
            2. En una flanera, verter el azúcar y llevar a fuego lento hasta conseguir que se vuelva caramelo. Cuando ello suceda, apaga el fuego y cubre las paredes de la flanera con el caramelo, deja enfriar.<br>
           3. Utiliza la licuadora para mezclar los huevos con la leche condensada y de coco, además de las esencias. Vierte el preparado en la flanera y lleva al horno en baño maría por espacio de 60 minutos aproximadamente.<br>
           5. Pasado el tiempo apaga el horno, pero no saques la flanera hasta que se enfríe.<br>
           6. Refrigera hasta que sea momento de servir.</p>
