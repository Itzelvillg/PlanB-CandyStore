      <div class="card-body">
        <h5 class="card-title">Crema de almendras</h5>
        <p class="card-text"><b>Ingredientes: </b><br>
            250 gramos de almendras<br>
            Aceite de coco<br>
            Dátiles (opcional)<br>
           <b> Preparación:</b> <br>
           1. El primer paso para preparar crema de almendras es precalentar el horno a 180°C. <br>
            2. Esparcir las almendras en una bandeja de hornear e introducir en el horno, el tiempo aproximado que deben permanecer allí es de 10 minutos, sin embargo, se debe poner atención a la coloración de las almendras ya que este indicará cuando es el momento preciso para sacarlas.<br>
            3. Cuando las almendras tostadas estén completamente frías colocar en un procesador de alimentos.<br>
           4. Es probable que las almendras tiendan a pegarse en las paredes del procesador por lo que cada cierto tiempo se debe detener su funcionamiento para remover con ayuda de una espátula.<br>
            5. Verás que en pocos ciclos las almendras comenzarán a soltar su propio aceite propiciando la formación de la crema, se puede ayudar este proceso con unas gotas de aceite de coco.<br>
            6. Si se desea, se puede procesar algunos dátiles remojados en aceite de coco durante algunas horas que harán más atractivos el sabor y consistencia de la crema. Cuando ambas cremas estén listas, procesar en conjunto para homogenizar.</p>
