        <h5 class="card-title">Papas con chimichurri</h5>
        <p class="card-text"><b>Ingredientes: </b> <br>
            1 kg. de papas cortadas al estilo francés.<br>
            30 gramos de chimi seco.<br>
            Albahaca.<br>
            1 taza de agua.<br>
            5 cdas. de pickles de zanahoria.<br>
            Sal, pimienta y cúrcuma a gusto.<br>
             <b>Preparación:</b> <br>
            1. Cortar las papas al estilo francés o en la forma que más se guste.<br>
            2. Colocar las papas en un recipiente con agua y sal y llevar a refrigerar por no menos de una hora. <br>
            3. Hidratar el chimi en una taza de agua.<br>
            4. En una fuente para horno agregar un chorrito de aceite, luego, poner las papas junto con los pickles y el chimi. Si los pickles son caseros, también se debe añadir un chorrito de vinagre a la preparación para que el sabor no varíe.<br>
            5. Precalentar el horno y hornear a baja temperatura.<br>
            6. Servir y agregar sal en caso sea necesario.</p>
