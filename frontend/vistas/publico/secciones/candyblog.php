        <h5 class="card-title">Bizcocho casero de yogur</h5>
        <p class="card-text"><b>Ingredientes: </b> <br>
            125 gr. de yogur natural.<br>
            350 gr. de harina.<br>
            250 gr. de azúcar.<br>
            120 ml. de aceite.<br>
            3 huevos.<br>
            10 gr. de levadura en polvo.<br>
            <b>Preparación: </b> <br>
            1. En un recipiente de regular tamaño bate el azúcar con los huevos, puedes hacerlo a mano o utilizar una batidora eléctrica, el único requerimiento es hacerlo hasta que la mezcla crezca hasta casi el doble.<br>
            2. Agrega el aceite y luego el yogur.<br>
           3. Mezcla hasta que todos los ingredientes se integren.<br>
            4. Es el turno de los ingredientes secos, tamiza la harina junto con la levadura y añade poco a poco para evitar la formación de grumos.<br>
           5. Vierte el preparado en un molde engrasado.<br>
           6. Lleva al horno utilizando una temperatura de 180°C. durante 40 minutos aproximadamente.<br>
            7. Deja enfriar y retira del molde. Puedes servirlo como está o bien decorarlo como tu prefieras.</p>
     
