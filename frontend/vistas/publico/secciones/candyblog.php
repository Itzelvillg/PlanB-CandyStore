      <div class="card-body">
        <h5 class="card-title">Mermelada de naranja</h5>
        <p class="card-text"> <b>Ingredientes:</b><br>
            4 naranjas<br>
            500 gramos de azúcar<br>
            1 limón<br>
            250 ml de agua (1 vaso)<br>
            <b>Preparación:</b><br>
            1. Lavar y pelar las naranjas, asegurándose de quitar muy bien la parte blanca -es lo que le puede dar gusto amargo a la preparación.<br>
            2. Cortar las naranjas en cubos o trozos pequeños y asegurarse de que no queden semillas.<br>
            3.Cortar una de las cáscaras de naranja en juliana.<br>
            4. En una cacerola con 250 ml de agua, poner a cocer a fuego lento la pulpa de las naranjas y la cáscara.<br>
            5. Exprimir un limón y cortar su cáscara en juliana.<br>
            6. Agregar el jugo de limón y la cáscara del mismo a la cacerola con la preparación de naranjas y dejarlo cocinar hasta que llegue a su punto de ebullición.<br>
            7. Luego añadir el azúcar, revolver bien y dejarla cocinar hasta que la mezcla se reduzca.<br>
           8. Seguir revolviendo de vez en cuando por aprox 40 minutos.<br>
            9. Cuando la mezcla espese, apagar el fuego.<br>
            10. Dejar reposar 5 minutos y verter en un recipiente para que se enfríe. Si se desea, puede verterse la mezcla en recipientes pequeños y llevar a la heladera para que enfríe más rápido.</p>
